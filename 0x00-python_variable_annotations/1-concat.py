#!/usr/bin/env python3
"""this module contain function concat"""


def concat(str1: str, str2: str) -> str:
    """
      desc:
        takes a string str1 and a string str2 as arguments and
        returns a concatenated string.
      args:
        str1 : string
        str2 : string
      return:
        concatination of str1 and str2
    """
    return str1 + str2
